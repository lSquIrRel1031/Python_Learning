# 定义一个变量，用来记录钱包余额的值
WalletBalance = 50
# 通过print语句，输出变量记录的内容
print("钱包余额为：", WalletBalance, "元")

WalletBalance = WalletBalance - 10
print("钱包余额为：", WalletBalance, "元")

# 假设：每隔一小时，输出钱包余额
print("现在是下午1点，钱包余额为", WalletBalance, "元")
